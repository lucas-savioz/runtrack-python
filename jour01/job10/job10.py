# ETAPE 1 

initial_invest = 5000
taux_rendement_annuel = 0.02

# Calcul rendement total annuel
rendement_annuel = initial_invest * taux_rendement_annuel

print("Investissment initial :", initial_invest)
print("Taux de rendement annuel :", taux_rendement_annuel)

# Gain total après un année
print("Capitable après investissement : ")
print(initial_invest + rendement_annuel)

